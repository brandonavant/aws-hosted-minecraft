"""Idempotent provisioner for the LightSail Paper + Geyser + Floodgate Minecraft server.

Invocation::

    sudo python3 scripts/server/install.py
    sudo python3 scripts/server/install.py --mc-version 1.21.8 --paper-build 60
    sudo python3 scripts/server/install.py --dry-run

This script runs on the LightSail instance after Terraform has provisioned the instance and the data volume has
been re-attached and mounted at ``/srv/minecraft``. It does NOT touch AWS resources; those live in ``infra/``.

Idempotent: every mutation step inspects current state and skips when the desired state is already in place.
Re-running converges to the same end state without error. ``--dry-run`` prints every mutation the script would
perform.

Pin policy: Paper, Geyser, Floodgate, and ViaVersion versions are pinned via CLI flags. Defaults read from the
data volume (Paper from ``version_history.json``; plugins from each jar's ``plugin.yml``). The script never
auto-bumps versions; that is the future nightly updater's job.

CRITICAL: ``plugins/floodgate/key.pem`` must be preserved across rebuilds. The script refuses to proceed if it is
absent (override with ``--allow-fresh-floodgate-key`` for a brand-new environment where Floodgate will generate
one on first start). Geyser and Floodgate share this key for Bedrock auth; regenerating it breaks login for every
existing Bedrock player.
"""

