# --- Main orchestration ------------------------------------------------------


@dataclass(frozen=True)
class ResolvedVersions:
    """Versions and builds the installer will use this run.

    Attributes:
        mc_version: Minecraft version (e.g. ``"1.21.8"``).
        paper_build: Paper build identifier (number, or ``"latest"``).
        geyser_version: Pinned Geyser version, or ``None`` for latest.
        geyser_build: Pinned Geyser build, or ``None`` for latest.
        floodgate_version: Pinned Floodgate version, or ``None`` for latest.
        floodgate_build: Pinned Floodgate build, or ``None`` for latest.
        viaversion_name: Pinned ViaVersion Hangar version name, or ``None``.
    """

    mc_version: str
    paper_build: int | str
    geyser_version: str | None
    geyser_build: int | None
    floodgate_version: str | None
    floodgate_build: int | None
    viaversion_name: str | None


def resolve_versions(args: argparse.Namespace, paths: InstallPaths) -> ResolvedVersions:
    """Pin every artifact's version, defaulting from the volume when available.

    Args:
        args: Parsed CLI arguments.
        paths: Resolved install paths.

    Returns:
        A populated ``ResolvedVersions``.

    Raises:
        VersionDiscoveryError: When ``--mc-version`` is not provided and the volume does not contain
            ``version_history.json`` to discover from.
    """
    mc_version = args.mc_version
    paper_build: int | str = args.paper_build if args.paper_build is not None else "latest"
    if mc_version is None:
        discovered = read_mc_version_from_history(paths.server_dir)
        if discovered is None:
            raise VersionDiscoveryError(
                "--mc-version is required when the data volume has no "
                f"{paths.server_dir / VERSION_HISTORY_FILE} (brand-new environment). "
                "Pass the MC version you want to install."
            )
        mc_version, discovered_build = discovered
        if args.paper_build is None:
            paper_build = discovered_build
    return ResolvedVersions(
        mc_version=mc_version,
        paper_build=paper_build,
        geyser_version=args.geyser_version,
        geyser_build=args.geyser_build,
        floodgate_version=args.floodgate_version,
        floodgate_build=args.floodgate_build,
        viaversion_name=args.viaversion,
    )


def install(args: argparse.Namespace) -> int:
    """Run the full provisioning pipeline.

    Args:
        args: Parsed CLI arguments.

    Returns:
        Process exit code. ``0`` on success.

    Raises:
        InstallError: Surfaced from any step. ``main`` catches and converts to a non-zero exit code.
    """
    paths = InstallPaths.from_data_root(args.data_root)

    # Pre-flight.
    assert_root()
    assert_ubuntu_2204(Path("/etc/os-release").read_text(encoding="utf-8"))
    if not args.skip_volume_check:
        assert_volume_mounted(paths.data_root, Path("/proc/mounts").read_text(encoding="utf-8"))

    # Apt: keyring → repo → update → install.
    keyring_added = install_corretto_keyring(dry_run=args.dry_run)
    repo_added = install_corretto_repo(dry_run=args.dry_run)
    if keyring_added or repo_added:
        apt_update(dry_run=args.dry_run)
    apt_install([*COMMON_PACKAGES, JAVA_PACKAGE], dry_run=args.dry_run)

    # User + group + directory layout.
    ensure_group(args.minecraft_group, dry_run=args.dry_run)
    ensure_user(
        args.minecraft_user,
        group=args.minecraft_group,
        home=paths.data_root,
        shell=args.minecraft_shell,
        dry_run=args.dry_run,
    )
    ensure_directory(
        paths.server_dir,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )
    ensure_directory(
        paths.plugins_dir,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )

    # Floodgate key precondition (after directories exist so the path resolves).
    assert_floodgate_key_present(paths.plugins_dir, allow_fresh=args.allow_fresh_floodgate_key)

    # Versions.
    versions = resolve_versions(args, paths)
    print(f"[plan] mc={versions.mc_version} paper={versions.paper_build}", file=sys.stderr)

    # Paper.
    paper_artifact = fetch_paper_artifact(versions.mc_version, versions.paper_build)
    download_and_place_jar(
        paper_artifact,
        paths.paper_jar,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )

    # Plugins.
    geyser_artifact = fetch_geyser_like_artifact(
        "geyser",
        GEYSER_API_BASE,
        version=versions.geyser_version,
        build=versions.geyser_build,
    )
    download_and_place_jar(
        geyser_artifact,
        paths.plugins_dir / geyser_artifact.filename,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )
    floodgate_artifact = fetch_geyser_like_artifact(
        "floodgate",
        FLOODGATE_API_BASE,
        version=versions.floodgate_version,
        build=versions.floodgate_build,
    )
    download_and_place_jar(
        floodgate_artifact,
        paths.plugins_dir / floodgate_artifact.filename,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )
    viaversion_artifact = fetch_viaversion_artifact(versions.viaversion_name)
    download_and_place_jar(
        viaversion_artifact,
        paths.plugins_dir / viaversion_artifact.filename,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )

    # Configs + launcher.
    ensure_eula(paths, args.minecraft_user, args.minecraft_group, dry_run=args.dry_run)
    ensure_start_script(
        paths,
        heap=args.heap_size,
        owner_user=args.minecraft_user,
        owner_group=args.minecraft_group,
        dry_run=args.dry_run,
    )

    # Systemd unit + service lifecycle.
    unit_changed = ensure_systemd_unit(
        paths,
        user=args.minecraft_user,
        group=args.minecraft_group,
        dry_run=args.dry_run,
    )
    if unit_changed:
        systemctl_daemon_reload(dry_run=args.dry_run)
    systemctl_enable_now(SYSTEMD_UNIT_NAME, dry_run=args.dry_run)

    # Updater deployment: copy update.py + _common.py to /usr/local/sbin and invoke the updater's own
    # self-install mode to drop the systemd units. The updater is the source of truth for its unit content
    # so install.py never emits update-timer/service templates directly.
    deploy_updater(Path(__file__).resolve().parent, dry_run=args.dry_run)
    invoke_updater_install_systemd_units(dry_run=args.dry_run)
    return 0


