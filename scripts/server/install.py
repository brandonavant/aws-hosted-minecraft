@dataclass(frozen=True)
class InstallPaths:
    """Resolved on-disk locations the installer operates against.

    Attributes:
        data_root: Mountpoint of the data volume (default ``/srv/minecraft``).
        server_dir: Paper server directory (``data_root/server``).
        plugins_dir: Plugins directory (``server_dir/plugins``).
        paper_jar: Path to the running Paper jar.
        start_script: Path to the launcher script ``start.sh``.
        eula_file: Path to ``eula.txt``.
    """

    data_root: Path
    server_dir: Path
    plugins_dir: Path
    paper_jar: Path
    start_script: Path
    eula_file: Path

    @classmethod
    def from_data_root(cls, data_root: Path) -> "InstallPaths":
        """Build an ``InstallPaths`` from the volume root.

        Args:
            data_root: Mountpoint of the data volume.

        Returns:
            A fully-populated ``InstallPaths``.
        """
        server_dir = data_root / SERVER_SUBDIR
        return cls(
            data_root=data_root,
            server_dir=server_dir,
            plugins_dir=server_dir / PLUGINS_SUBDIR,
            paper_jar=server_dir / PAPER_JAR_NAME,
            start_script=server_dir / START_SCRIPT_NAME,
            eula_file=server_dir / EULA_FILENAME,
        )


# --- Subprocess wrapper ------------------------------------------------------


def run_command(
    argv: Sequence[str],
    *,
    check: bool = True,
    capture: bool = False,
    dry_run: bool = False,
    input_text: str | None = None,
) -> subprocess.CompletedProcess[str]:
    """Run a local command, honoring ``--dry-run`` for mutating invocations.

    Args:
        argv: Command tokens. The first element is the executable.
        check: When true, raise ``CalledProcessError`` on non-zero exit. Use ``False`` for state-query commands
            where non-zero is informational.
        capture: When true, capture stdout/stderr as text. When false, the child process inherits the parent's
            stdio.
        dry_run: When true, log the command to stderr and return a zero-exit stub without executing. Callers
            should set this for mutating invocations only — read-only state queries should always run.
        input_text: Optional stdin to pipe in.

    Returns:
        The ``CompletedProcess``. In dry-run mode, ``returncode`` is ``0`` and stdout/stderr are empty strings.
    """
    rendered = " ".join(shlex.quote(t) for t in argv)
    if dry_run:
        print(f"[dry-run] {rendered}", file=sys.stderr)
        return subprocess.CompletedProcess(args=list(argv), returncode=0, stdout="", stderr="")
    print(f"[run] {rendered}", file=sys.stderr)
    return subprocess.run(  # noqa: S603 — argv passed as list, no shell.
        list(argv),
        check=check,
        capture_output=capture,
        text=True,
        input=input_text,
    )


# --- Pre-flight checks -------------------------------------------------------


def assert_root() -> None:
    """Refuse to proceed unless the process is running as UID 0.

    Raises:
        PreflightError: When ``geteuid()`` is non-zero.
    """
    if os.geteuid() != 0:
        raise PreflightError("install.py must run as root (re-invoke with sudo)")


def assert_ubuntu_2204(os_release_text: str) -> None:
    """Confirm the host is Ubuntu 22.04 by parsing ``/etc/os-release`` content.

    Args:
        os_release_text: Full content of ``/etc/os-release``.

    Raises:
        PreflightError: When the file does not identify an Ubuntu 22.04 host.
    """
    fields = parse_os_release(os_release_text)
    distro = fields.get("ID", "").strip().lower()
    version = fields.get("VERSION_ID", "").strip().strip('"')
    if distro != "ubuntu" or version != "22.04":
        raise PreflightError(f"unsupported OS: ID={distro!r} VERSION_ID={version!r}; expected ubuntu 22.04")


def parse_os_release(text: str) -> dict[str, str]:
    """Parse a simple ``KEY=VALUE`` os-release file.

    Args:
        text: Full file content.

    Returns:
        Dict of keys to unquoted values. Lines without ``=`` are ignored.
    """
    parsed: dict[str, str] = {}
    for raw in text.splitlines():
        if "=" not in raw or raw.lstrip().startswith("#"):
            continue
        key, _, value = raw.partition("=")
        key = key.strip()
        value = value.strip()
        if len(value) >= 2 and value[0] == value[-1] and value[0] in {'"', "'"}:
            value = value[1:-1]
        parsed[key] = value
    return parsed


def assert_volume_mounted(data_root: Path, mounts_text: str) -> None:
    """Confirm ``data_root`` is the mountpoint of a real filesystem (not the rootfs).

    Reads ``/proc/mounts``-style content. The intent is to refuse to write into a directory that's just a
    placeholder on the rootfs — world data must live on the attached block storage.

    Args:
        data_root: Expected mountpoint of the data volume.
        mounts_text: Content of ``/proc/mounts`` (or equivalent).

    Raises:
        PreflightError: When ``data_root`` does not appear as its own mount.
    """
    target = str(data_root)
    for line in mounts_text.splitlines():
        parts = line.split()
        if len(parts) >= 2 and parts[1] == target:
            return
    raise PreflightError(
        f"{data_root} is not a mountpoint — re-attach the data volume and verify the fstab entry "
        "before re-running install.py"
    )


# --- Apt operations ----------------------------------------------------------


def install_corretto_keyring(*, dry_run: bool = False) -> bool:
    """Install the Corretto apt signing keyring under ``/usr/share/keyrings/``.

    Idempotent: skipped when the keyring file already exists.

    Args:
        dry_run: When true, log the planned mutations without performing them.

    Returns:
        ``True`` when the keyring was installed by this call, ``False`` when it was already present.

    Raises:
        DownloadError: When the key cannot be fetched.
    """
    if CORRETTO_KEYRING_PATH.exists():
        print(f"[skip] {CORRETTO_KEYRING_PATH} already present", file=sys.stderr)
        return False
    if dry_run:
        print(f"[dry-run] download {CORRETTO_KEY_URL} → gpg --dearmor → {CORRETTO_KEYRING_PATH}", file=sys.stderr)
        return True
    key_bytes = http_get(CORRETTO_KEY_URL)
    CORRETTO_KEYRING_PATH.parent.mkdir(parents=True, exist_ok=True)
    proc = subprocess.run(  # noqa: S603 — argv as list, no shell.
        ["gpg", "--dearmor", "--output", str(CORRETTO_KEYRING_PATH)],
        input=key_bytes,
        capture_output=True,
        check=False,
    )
    if proc.returncode != 0:
        stderr = proc.stderr.decode("utf-8", errors="replace")
        raise InstallError(f"gpg --dearmor failed (rc={proc.returncode}): {stderr}")
    CORRETTO_KEYRING_PATH.chmod(0o644)
    return True


