def apt_packages_installed(packages: Sequence[str]) -> dict[str, bool]:
    """Return a mapping of package name → whether ``dpkg -s`` reports installed.

    Args:
        packages: Package names to query.

    Returns:
        Mapping of name to install status. Missing packages map to ``False``.
    """
    result: dict[str, bool] = {}
    for pkg in packages:
        proc = subprocess.run(  # noqa: S603 — argv as list, no shell.
            ["dpkg", "-s", pkg],
            capture_output=True,
            text=True,
            check=False,
        )
        result[pkg] = proc.returncode == 0 and "Status: install ok installed" in proc.stdout
    return result


def apt_install(packages: Sequence[str], *, dry_run: bool = False) -> Sequence[str]:
    """Install ``packages`` via apt, skipping any already installed.

    Args:
        packages: Package names to install.
        dry_run: When true, log the planned install without performing it.

    Returns:
        The list of packages that were actually installed (i.e. were missing before the call). Empty when nothing
        needed installing.
    """
    statuses = apt_packages_installed(packages)
    missing = [p for p, installed in statuses.items() if not installed]
    if not missing:
        print(f"[skip] all packages already installed: {', '.join(packages)}", file=sys.stderr)
        return ()
    run_command(
        ["env", "DEBIAN_FRONTEND=noninteractive", "apt-get", "install", "-y", "--no-install-recommends", *missing],
        dry_run=dry_run,
    )
    return tuple(missing)


# --- User / group operations -------------------------------------------------


def group_exists(name: str) -> bool:
    """Return whether a system group named ``name`` exists.

    Args:
        name: Group name.

    Returns:
        ``True`` when ``getent group <name>`` returns success.
    """
    proc = subprocess.run(  # noqa: S603 — argv as list, no shell.
        ["getent", "group", name],
        capture_output=True,
        check=False,
    )
    return proc.returncode == 0


def user_exists(name: str) -> bool:
    """Return whether a system user named ``name`` exists.

    Args:
        name: User name.

    Returns:
        ``True`` when ``getent passwd <name>`` returns success.
    """
    proc = subprocess.run(  # noqa: S603 — argv as list, no shell.
        ["getent", "passwd", name],
        capture_output=True,
        check=False,
    )
    return proc.returncode == 0


def ensure_group(name: str, *, dry_run: bool = False) -> bool:
    """Create a system group if it does not already exist.

    Args:
        name: Group name to create.
        dry_run: When true, log the planned mutation without performing it.

    Returns:
        ``True`` when the group was created by this call, ``False`` when it was already present.
    """
    if group_exists(name):
        print(f"[skip] group {name!r} already exists", file=sys.stderr)
        return False
    run_command(["groupadd", "--system", name], dry_run=dry_run)
    return True


def ensure_user(
    name: str,
    *,
    group: str,
    home: Path,
    shell: str,
    dry_run: bool = False,
) -> bool:
    """Create a system user with the given group, home, and shell if absent.

    Args:
        name: User name to create.
        group: Primary group (must already exist; create with ``ensure_group``).
        home: Home directory path. The directory is NOT created here.
        shell: Login shell (e.g. ``/usr/sbin/nologin``).
        dry_run: When true, log the planned mutation without performing it.

    Returns:
        ``True`` when the user was created by this call, ``False`` when it was already present.
    """
    if user_exists(name):
        print(f"[skip] user {name!r} already exists", file=sys.stderr)
        return False
    run_command(
        ["useradd", "--system", "--gid", group, "--home-dir", str(home), "--shell", shell, name],
        dry_run=dry_run,
    )
    return True


# --- File-write idempotency --------------------------------------------------


def write_file_if_changed(
    path: Path,
    content: str,
    *,
    owner_user: str | None,
    owner_group: str | None,
    mode: int,
    dry_run: bool = False,
) -> bool:
    """Write ``content`` to ``path`` only if the existing file differs.

    Compares as bytes after UTF-8 encoding. When the file is written, ownership and mode are applied.
    ``owner_user`` / ``owner_group`` may be ``None`` to leave ownership at the process default (root).

    Args:
        path: Target path.
        content: Desired UTF-8 text content.
        owner_user: Optional owning user name.
        owner_group: Optional owning group name.
        mode: Numeric mode bits (e.g. ``0o644``).
        dry_run: When true, log the planned write without performing it.

    Returns:
        ``True`` when the file was written by this call, ``False`` when the existing content already matched.
    """
    desired_bytes = content.encode("utf-8")
    if path.exists() and path.read_bytes() == desired_bytes:
        existing_mode = path.stat().st_mode & 0o7777
        if existing_mode == mode:
            print(f"[skip] {path} already at desired content + mode", file=sys.stderr)
            return False
    if dry_run:
        print(f"[dry-run] write {len(desired_bytes)} bytes to {path} (mode {oct(mode)})", file=sys.stderr)
        return True
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_bytes(desired_bytes)
    path.chmod(mode)
    if owner_user and owner_group:
        run_command(["chown", f"{owner_user}:{owner_group}", str(path)])
    return True


def ensure_directory(
    path: Path,
    *,
    owner_user: str,
    owner_group: str,
    mode: int = 0o755,
    dry_run: bool = False,
) -> bool:
    """Create ``path`` and apply ownership/mode if it does not already exist.

    When the directory already exists, ownership is fixed only when wrong (one fewer subprocess on a re-run). The
    mode is fixed in the same way.

    Args:
        path: Directory to create.
        owner_user: Owning user.
        owner_group: Owning group.
        mode: Numeric mode bits.
        dry_run: When true, log the planned mutation without performing it.

    Returns:
        ``True`` when ``mkdir`` ran, ``False`` when the directory existed.
    """
    created = False
    if not path.exists():
        if dry_run:
            print(f"[dry-run] mkdir -p {path} (mode {oct(mode)} owner {owner_user}:{owner_group})", file=sys.stderr)
            return True
        path.mkdir(parents=True, exist_ok=True)
        created = True
    if not dry_run:
        path.chmod(mode)
        run_command(["chown", f"{owner_user}:{owner_group}", str(path)])
    return created


# --- Download + place --------------------------------------------------------


def download_and_place_jar(
    artifact: JarArtifact,
    dest: Path,
    *,
    owner_user: str,
    owner_group: str,
    dry_run: bool = False,
) -> bool:
    """Download ``artifact`` to ``dest`` if missing or sha256-mismatched.

    Verifies the downloaded bytes against ``artifact.sha256`` and applies ownership. A pre-existing file with the
    matching hash is left in place.

    Args:
        artifact: Jar metadata.
        dest: Final on-disk path.
        owner_user: Owning user for the placed file.
        owner_group: Owning group for the placed file.
        dry_run: When true, log the planned download without performing it.

    Returns:
        ``True`` when the jar was downloaded by this call, ``False`` when the existing file already matched.

    Raises:
        ChecksumMismatchError: When the downloaded bytes do not match.
        DownloadError: When the HTTP fetch fails.
    """
    if dest.exists():
        observed = sha256_of_file(dest)
        if observed == artifact.sha256:
            print(f"[skip] {dest} already at sha256 {artifact.sha256[:12]}…", file=sys.stderr)
            return False
        print(f"[update] {dest} sha256 {observed[:12]}… → {artifact.sha256[:12]}…", file=sys.stderr)
    if dry_run:
        print(f"[dry-run] download {artifact.label} {artifact.version} → {dest}", file=sys.stderr)
        return True
    dest.parent.mkdir(parents=True, exist_ok=True)
    print(f"[download] {artifact.label} {artifact.version} → {dest}", file=sys.stderr)
    http_download_to_file(artifact.download_url, dest)
    observed = sha256_of_file(dest)
    if observed != artifact.sha256:
        dest.unlink()
        raise ChecksumMismatchError(
            f"{artifact.label} {artifact.version}: expected sha256 {artifact.sha256}, got {observed}"
        )
    run_command(["chown", f"{owner_user}:{owner_group}", str(dest)])
    return True


# --- Floodgate key check -----------------------------------------------------


def assert_floodgate_key_present(plugins_dir: Path, *, allow_fresh: bool) -> None:
    """Refuse to proceed when ``floodgate/key.pem`` is missing without override.

    Floodgate's ``key.pem`` signs Bedrock auth handshakes; Geyser's ``floodgate-key-file`` config points at it.
    Regenerating the key breaks login for every existing Bedrock player. The script preserves the volume's key
    when present; ``allow_fresh`` is required to opt into a brand-new environment where Floodgate will generate
    one on first start.

    Args:
        plugins_dir: Plugins directory (``data_root/server/plugins``).
        allow_fresh: When true, a missing key is acceptable (caller is standing up a brand-new environment, not
            rebuilding the live one).

    Raises:
        FloodgateKeyMissingError: When the key is absent and ``allow_fresh`` is false.
    """
    key_path = plugins_dir / "floodgate" / "key.pem"
    if key_path.exists():
        print(f"[ok] preserving Floodgate key at {key_path}", file=sys.stderr)
        return
    if allow_fresh:
        print(
            f"[warn] {key_path} missing; --allow-fresh-floodgate-key set, Floodgate will "
            "generate a new key on first start (existing Bedrock players will need to re-link)",
            file=sys.stderr,
        )
        return
    raise FloodgateKeyMissingError(
        f"{key_path} missing. The Floodgate key MUST be preserved across rebuilds, "
        "otherwise existing Bedrock players cannot log in. Restore the key from a "
        "snapshot, or pass --allow-fresh-floodgate-key if you are standing up a "
        "brand-new environment."
    )


